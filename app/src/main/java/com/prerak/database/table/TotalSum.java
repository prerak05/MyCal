public class TotalSum {
    private String interst;
    private String totalAmount;
    private String totalInterestedAmount;


    public String getInterst() {
        return interst;
    }

    public void setInterst(String interst) {
        this.interst = interst;
    }

    public String getTotalAmount() {
        return totalAmount;
    }

    public void setTotalAmount(String totalAmount) {
        this.totalAmount = totalAmount;
    }


    public String getTotalInterestedAmount() {
        return totalInterestedAmount;
    }

    public void setTotalInterestedAmount(String totalInterestedAmount) {
        this.totalInterestedAmount = totalInterestedAmount;
    }
}
